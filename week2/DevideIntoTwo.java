package com.example.javaproject3.week2;

public class DevideIntoTwo {
    public static void main(String[] args) {
        float fOne = 1;
        float fTwo = 2;

        System.out.printf("%f\n", fOne + fOne / fTwo);

        int iOne = 1;
        int iTwo = 2;
        System.out.printf("%d\n", iOne + iOne / iTwo);
    }
}
